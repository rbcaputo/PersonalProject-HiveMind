namespace HiveMind.Core.Enums
{
  /// <summary>
  /// Defines the different types of bees in the colony based on their biological castes.
  /// </summary>
  public enum BeeType
  {
    /// <summary>
    /// Worker bee - sterile female responsible for foraging, building, and hive maintenance.
    /// </summary>
    Worker = 1,

    /// <summary>
    /// Drone bee - male bee whose primary purpose is reproduction.
    /// </summary>
    Drone = 2,

    /// <summary>
    /// Queen bee - fertile female responsible for laying eggs and leading the colony.
    /// </summary>
    Queen = 3
  }
}
