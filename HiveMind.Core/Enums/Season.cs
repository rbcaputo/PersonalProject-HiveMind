namespace HiveMind.Core.Enums
{
  /// <summary>
  /// Defines the four seasons that affect bee behavior and colony lifecycle.
  /// </summary>
  public enum Season
  {
    /// <summary>
    /// Spring - colony expansion, high activity, queen lays many eggs.
    /// </summary>
    Spring = 1,

    /// <summary>
    /// Summer - peak activity, maximum foraging, honey production.
    /// </summary>
    Summer = 2,

    /// <summary>
    /// Autumn - preparing for winter, reduced laying, storing resources.
    /// </summary>
    Autumn = 3,

    /// <summary>
    /// Winter - survival mode, cluster formation, minimal activity.
    /// </summary>
    Winter = 4
  }
}
